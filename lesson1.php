// В заданном массиве найти сумму четных чисел
function findSum()
{
    $a = [1000, -2, -3, 9, 4];

    $sum = 0;
    for ($i = 0; $i < count($a); $i++) {
        if ($a[$i] % 2 == 0) {
            $sum += $a[$i];
        }
    }

    echo $sum;
}

// В заданном массиве найти количество чисел, соседи у которых нечетные
// [1000, 1, -3, 1, 4] -> 1 число
function countNumbers()
{
    $a = [1000, 1, 1, 1, 1];

    $count = 0;
    for ($i = 1; $i < count($a) - 1; $i++) {
        if ($a[$i - 1] % 2 == 1 && $a[$i + 1] % 2 == 1) {
            $count++;
        }
    }

    echo $count;
}

// Дан массив. Является ли он возрастающим
// [1, 4, 6, 8, 10, 34] -> да
// [1, 4, 6, 6, 10, 34] -> нет
// [1, 4, 6, -4, 10, 34] -> нет
function isUp()
{
    $a = [1, 4, 6, 8, 10, 34];

    $isUp = true;
    for ($i = 0; $i < count($a) - 1; $i++) {
        if ($a[$i] >= $a[$i + 1]) {
            $isUp = false;
            break;
        }
    }

    if ($isUp) {
        echo 'Yes';
    } else {
        echo 'No';
    }
}

