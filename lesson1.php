function findNumbers()
{
// 1 способ
    for ($n = 100; $n <= 999; $n++) {
        $a = $n % 10; // последняя цифра
        $b = intdiv($n, 10) % 10; // средняя цифра
        $c = intdiv($n, 100); // первая справа цифра

        if ($a == $b || $a == $c || $b == $c) {
            echo $n . ' - yes<br>';
        } else {
            echo $n . ' - no<br>';
        }
    }
}

